//! App-wide constants.
//!
//! # Bounded Context: Identity & Configuration Names
//!
//! Single source of truth for the app name, config filenames, env var
//! names, version strings, and URLs. Every other module imports from
//! here — a rename only requires changing this file.

// ── Version & build metadata ────────────────────────────────────────

/// Semantic version from Cargo.toml (patched from the git tag in CI).
pub const VERSION: &str = env!("CARGO_PKG_VERSION");

/// Full version string including dev metadata.
///
/// For release builds (HEAD tagged `v{VERSION}`) this equals [`VERSION`].
/// For development builds it appends `-dev+{GIT_SHA}` so that telemetry
/// and diagnostics can distinguish local builds from released ones.
pub const FULL_VERSION: &str = env!("FULL_VERSION");

/// Short git commit hash at build time, or `"unknown"`.
pub const GIT_SHA: &str = env!("GIT_SHA");

/// Build date (UTC, `YYYY-MM-DD`), or `"unknown"`.
pub const BUILD_DATE: &str = env!("BUILD_DATE");

/// Compilation target triple (e.g. `x86_64-unknown-linux-gnu`).
pub const TARGET: &str = env!("TARGET");

/// User-Agent header for HTTP requests.
pub const USER_AGENT: &str = concat!("chronikl/", env!("CARGO_PKG_VERSION"));

// ── App identity ────────────────────────────────────────────────────

/// Display name of the tool (lowercase).
pub const APP_NAME: &str = "chronikl";

/// Local config filename (e.g. `.chronikl.toml` in repo root).
pub const CONFIG_FILENAME: &str = ".chronikl.toml";

/// Directory name under `~/.config/` for global config and cache.
pub const CONFIG_DIR: &str = "chronikl";

/// AI-generated content disclosure notice.
pub const AI_DISCLOSURE: &str =
    "Generated by chronikl — AI-powered release notes. Review before publishing.";

/// Telemetry heartbeat endpoint. Placeholder until the chronikl.dev
/// endpoint is provisioned; opt-out via `--no-telemetry` /
/// `CHRONIKL_TELEMETRY=false` is honoured regardless.
pub const TELEMETRY_URL: &str = "https://chronikl.dev/v1/heartbeat";

// ── GitHub releases (self-update) ───────────────────────────────────

/// GitHub owner/repo for release downloads.
pub const GITHUB_REPO: &str = "nsrosenqvist/chronikl";

/// GitHub API endpoint for the latest release metadata.
pub const GITHUB_RELEASES_LATEST_API: &str =
    "https://api.github.com/repos/nsrosenqvist/chronikl/releases/latest";

/// Build a download URL for a release asset.
pub fn release_asset_url(tag: &str, target: &str) -> String {
    format!(
        "https://github.com/{}/releases/download/{}/chronikl-{}.tar.gz",
        GITHUB_REPO, tag, target
    )
}

/// Build a download URL for the SHA256SUMS file of a release.
pub fn release_checksums_url(tag: &str) -> String {
    format!(
        "https://github.com/{}/releases/download/{}/SHA256SUMS",
        GITHUB_REPO, tag
    )
}

