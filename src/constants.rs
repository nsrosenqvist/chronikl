pub const HTTP_CONNECT_TIMEOUT: std::time::Duration = std::time::Duration::from_secs(5);

// ── Ladder defaults ─────────────────────────────────────────────────

/// Per-call output cap for Tier 1 batched classification. Generous —
/// a 50-commit batch produces tiny per-entry verdicts.
pub const TIER1_MAX_OUTPUT_TOKENS: u64 = 8000;

/// Per-call output cap for Tier 2 per-commit classification. One
/// verdict object per call; small.
pub const TIER2_MAX_OUTPUT_TOKENS: u64 = 2000;

/// Per-call output cap for Tier 3 agent-loop turns. Higher than Tier 2
/// because agent turns can include text + tool calls.
pub const TIER3_MAX_OUTPUT_TOKENS: u64 = 4000;

/// Per-call output cap for the prose pass. Large because it produces
/// the entire release-notes document.
pub const PROSE_MAX_OUTPUT_TOKENS: u64 = 8000;

/// Maximum number of in-flight LLM calls per tier. Bounds concurrency
/// so we don't trigger provider rate limits while still amortizing
/// network latency across batches / per-commit calls.
pub const LLM_CONCURRENCY: usize = 8;

/// Maximum number of in-flight PR-platform API requests during
/// enrichment. Higher than LLM concurrency because forge REST APIs
/// (GitHub today, GitLab/Bitbucket later) are much faster per-call
/// and tolerate more parallelism.
pub const PR_CONCURRENCY: usize = 16;

/// Tier 1 batch size: how many commits go into a single classification call.
pub const DEFAULT_BATCH_SIZE: usize = 50;

/// Tier 2 confidence threshold: commits below this score get re-classified
/// with their diff included.
pub const DEFAULT_CONFIDENCE_THRESHOLD: f32 = 0.6;

/// Tier 2 maximum diff tokens per commit before truncation.
pub const DEFAULT_MAX_DIFF_TOKENS: usize = 4000;
